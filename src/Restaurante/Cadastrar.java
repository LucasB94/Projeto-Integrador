    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = txtNome.getText();
        String contato = ftxtContato.getText();
        String CPF = ftxtCPF.getText();
        String email = txtEmail.getText();
                

        boolean validaCont = contato.matches("[0-9]{2}[0-9]{5}[0-9]{4}");
        boolean validaCPF = CPF.matches("[0-9]{3}[0-9]{3}[0-9]{3}[0-9]{2}");
        boolean validaEmail = email.matches("\\\\w+@\\\\w+\\\\.\\\\w{2,3}|\\\\w+@\\\\w+\\\\.\\\\w{2,3}.\\\\w{2,3}");

        try {

            if (nome.isEmpty() || contato.isEmpty() || CPF.isEmpty() || email.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!");
            } else if (validaCont == true) {
                JOptionPane.showMessageDialog(null, "Contato neste formato: (XX)XXXXX-XXXX");
            } else if (validaCPF == true) {
                JOptionPane.showMessageDialog(null, "CPF neste formato: XXX.XXX.XXX-XX");
            } else if (validaEmail == true) {
                JOptionPane.showMessageDialog(null, "E-mail neste formato: \n dominio@email.com.br ou \n dominio@email.com");
            } else {
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
            }
        } catch (IllegalStateException e) {
            System.out.println("Erro Detectado " + e.getMessage());
        }
        
        
        try{
        Cadastro cadastro = new Cadastro();
        
        cadastro.setNome(nome);
        cadastro.setContato(contato);
        cadastro.setCPF(CPF);
        cadastro.setEmail(email);
        
        OsirisDAO osirisdao = new OsirisDAO();
        osirisdao.InserirCadastro(cadastro);
        
        }catch(Exception e){
             System.out.println("Erro do Cadastro: " + e.getMessage());
        }
        

    }//GEN-LAST:event_btnCadastrarActionPerformed

